import enoslib as en

logging = en.init_logging()

provider_conf = {
    "resources": {
        "machines": [
            {
                "roles": ["control"],
                "address": "localhost",
                "alias": "test_machine",
                "extra": {"ansible_connection": "local"},
            }
        ],
        "networks": [
            {
                "roles": ["local"],
                "start": "172.17.0.0",
                "end": "172.17.255.255",
                "cidr": "172.17.0.0/16",
                "gateway": "172.17.0.1",
                "dns": "172.17.0.1",
            }
        ],
    }
}

conf = en.StaticConf().from_dictionary(provider_conf)
provider = en.Static(conf)
roles, networks = provider.init()
result = en.gather_facts(roles=roles)
print(result)
